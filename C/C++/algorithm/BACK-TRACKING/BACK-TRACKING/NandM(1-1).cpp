#include <iostream>

using namespace std;
#define MAX 9

int N, M;
int arr[MAX];
bool visited[MAX];

void dfs(int depth) { // 현재 위치
	if (depth == M) { // 목표인 M까지 도달했다면
		for (auto i = 0; i < M; ++i)
			cout << arr[i] << " "; // arr에 저장한 값 M개 만큼 출력
		cout << endl;
	}
	else { // 목표에 도달하지 않았다면
		for (auto i = 1; i <= N; ++i) {
			if (!visited[i]) { // 방문 안했다면
				visited[i] = true; // 방문 표시
				arr[depth] = i; // i 값을 arr에 저장
				dfs(depth + 1); // M까지 재귀
				visited[i] = false; // 백트래킹 설정
			}
		}
	}
}

int main() {
	
	cin >> N >> N;

	return 0;
}